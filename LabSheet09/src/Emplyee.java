
public class Emplyee {
	private String id;
	private String name;
	private double salary;
	
	Emplyee(String id,String name,double salary){
		this.id =id;
		this.name =name;
		this.salary =salary;
	}
	Emplyee(String id,String name){
		this.id =id;
		this.name =name;
	}
	
	public String getId() {
		return this.id =id;
	}
	
	public String getName() {
		return this.name =name;
		}
	
	public double getSalary() {
		return this.salary =salary;
	}
	
}
